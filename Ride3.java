package CabInvoiceGenerator;

public class Ride3 {

    public double distance;
    public double time;
    public Ride3(double distance, double time)
    {
        super();
        this.distance = distance;
        this.time = time;
    }
    @Override
    public String toString()
    {
        return "Ride3 [distance=" + distance + ", time=" + time + "]";
    }
}
