package CabInvoiceGenerator;

public class Ride4 {


    public double distance;
    public double time;

    public Ride4(double distance, double time) {
        super();
        this.distance = distance;
        this.time = time;
    }

    @Override
    public String toString() {
        return "Ride [distance=" + distance + ", time=" + time + "]";
    }

}